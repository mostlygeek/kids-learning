package main

import (
	"fmt"
	"math/rand"
	"time"

	"github.com/mostlygeek/kids-learning/lib"
)

func init() {
	rand.Seed(time.Now().UnixNano())
}

// https://kateshomeschoolmath.com/everything-you-need-teach-addition-facts/
// this is set 1,  adding +1 and +2
func main() {
	pdf := lib.NewPDF()

	lib.AddTablePage(pdf, []string{
		"1 + 2 = ____", "2 + 1 = ____", " ", " ",
		"2 + 2 = ____", "2 + 2 = ____", " ", " ",
		"3 + 2 = ____", "2 + 3 = ____", " ", " ",
		"4 + 2 = ____", "2 + 4 = ____", " ", " ",
		"5 + 2 = ____", "2 + 5 = ____", " ", " ",
		"6 + 2 = ____", "2 + 6 = ____", " ", " ",
		"7 + 2 = ____", "2 + 7 = ____", " ", " ",
		"8 + 2 = ____", "2 + 8 = ____", " ", " ",
		"9 + 2 = ____", "2 + 9 = ____", " ", " ",
		"            ", "            ", " ", " ",
		"            ", "            ", " ", " ",
		"            ", "            ", " ", " ",
	})

	lib.AddTablePage(pdf, []string{
		"2 + 1 = ____", "4 + 2 = ____", "5 + 2 = ____", "8 + 2 = ____",
		"2 + 2 = ____", "5 + 2 = ____", "2 + 4 = ____", "5 + 2 = ____",
		"2 + 3 = ____", "6 + 2 = ____", "4 + 2 = ____", "8 + 2 = ____",
		"2 + 4 = ____", "7 + 2 = ____", "2 + 7 = ____", "9 + 2 = ____",
		"2 + 5 = ____", "8 + 2 = ____", "2 + 2 = ____", "6 + 2 = ____",
		"2 + 6 = ____", "2 + 9 = ____", "2 + 4 = ____", "2 + 2 = ____",
		"2 + 7 = ____", "9 + 2 = ____", "2 + 3 = ____", "7 + 2 = ____",
		"2 + 8 = ____", "2 + 8 = ____", "2 + 9 = ____", "5 + 2 = ____",
		"2 + 9 = ____", "8 + 2 = ____", "2 + 1 = ____", "8 + 2 = ____",
		"1 + 2 = ____", "2 + 6 = ____", "1 + 2 = ____", "2 + 2 = ____",
		"2 + 2 = ____", "6 + 2 = ____", "5 + 2 = ____", "4 + 2 = ____",
		"3 + 2 = ____", "2 + 5 = ____", "4 + 2 = ____", "6 + 2 = ____",
	})

	list := make([]string, 48, 48)
	for i := 0; i < 48; i++ {
		n := (i % 8) + 1
		r := rand.Intn(3)
		if r == 0 {
			list[i] = fmt.Sprintf("%d - 2 = ____", 2+n)
		} else if r == 1 {
			list[i] = fmt.Sprintf("2 + %d = ____", n)
		} else {
			list[i] = fmt.Sprintf("%d + 2 = ____", 2)
		}
	}
	lib.AddTablePage(pdf, list)

	if err := pdf.OutputFileAndClose("output-add-set1.pdf"); err != nil {
		fmt.Println(err.Error())
	}
}
